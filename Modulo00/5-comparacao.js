console.log("igual a algo")
console.log(3 == 3);
console.log(3 == 4);
console.log(10 == "10");
console.log(3 / 3 == 1);
console.log(3 === "3"); // são iguais, dependendo do valor e do tipo do atributo

console.log("diferente de algo")
console.log(3 != 5);
console.log(3 != "5");
console.log(3 !== "3"); // são diferentes, dependendo do valor e do tipo do atributo

console.log("maior que algo")
console.log(5 > 0);
console.log(5 > 9);
console.log(5 > 5);
console.log(5 >= 5);
console.log(15 <= 6);
console.log("a" < "g"); // a vem depois de g --> retorna true
console.log("l" < "k"); // l vem antes de k --> retorna false 
console.log("r" > "s"); // r vem antes de s --> retorna false
console.log("t" > "o"); // t vem antes de o --> retorna true